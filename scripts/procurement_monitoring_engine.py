@dataclass
class MonitoringAlert:
    """A single early-warning alert generated by PSE-7A."""
    alert_id: str
    title: str
    severity: str              # INFO / NOTICE / WARNING / HIGH / CRITICAL
    source: str                # LOCAL / IMPORTED / PORTFOLIO / SYSTEM
    reason: str
    business_impact: str
    recommended_action: str
    expected_urgency: str      # when this requires executive attention
    metric_value: Optional[float]
    threshold_value: Optional[float]
    triggered_at: str          # ISO date

    def to_dict(self) -> dict:
        return asdict(self)


@dataclass
class WatchlistItem:
    """One executive watchlist entry — one-line headline for daily briefing."""
    item_id: str
    category: str              # INVENTORY / PRICE / TIMING / OPPORTUNITY / RISK / SYSTEM
    headline: str
    detail: str
    days_until_event: Optional[int]
    severity: str

    def to_dict(self) -> dict:
        return asdict(self)


@dataclass
class ExecutiveMonitoringReport:
    """PSE-7A — Full executive early warning report."""
    run_date: str
    alerts: list
    watchlist: list
    upcoming_procurement_events: list
    upcoming_risk_events: list
    upcoming_savings_opportunities: list
    summary: str
    highest_severity: str
    alert_count_by_severity: dict

    def to_dict(self) -> dict:
        return {
            "run_date":    self.run_date,
            "alerts":      [a.to_dict() if hasattr(a, "to_dict") else a
                            for a in self.alerts],
            "watchlist":   [w.to_dict() if hasattr(w, "to_dict") else w
                            for w in self.watchlist],
            "upcoming_procurement_events":  self.upcoming_procurement_events,
            "upcoming_risk_events":         self.upcoming_risk_events,
            "upcoming_savings_opportunities": self.upcoming_savings_opportunities,
            "summary":               self.summary,
            "highest_severity":      self.highest_severity,
            "alert_count_by_severity": self.alert_count_by_severity,
        }


