def _build_watchlist(
    alerts: list,
    exec_report_dict: dict,
    scenario_report_dict: dict,
    local_inv: float,
    imported_inv: float,
    total_inv: float,
    today: date,
) -> list:
    """Build the executive watchlist from alerts and upcoming events."""
    watchlist: list[WatchlistItem] = []
    _id = [0]

    def _w(category, headline, detail, days_until_event, severity):
        _id[0] += 1
        return WatchlistItem(
            item_id=f"WL_{_id[0]:03d}",
            category=category,
            headline=headline,
            detail=detail,
            days_until_event=days_until_event,
            severity=severity,
        )

    # ── Inventory status ────────────────────────────────────────────────────
    if local_inv > 0:
        days_l = (local_inv - LOCAL_ROP_TONS) / DAILY_CONSUMPTION_LOCAL
        if days_l > 0:
            watchlist.append(_w(
                "INVENTORY",
                f"Local inventory will reach reorder point in ~{days_l:.0f} days.",
                f"Current: {local_inv:,.0f} t  |  ROP: {LOCAL_ROP_TONS:,.0f} t  |  "
                f"Consumption: {DAILY_CONSUMPTION_LOCAL:.1f} t/day",
                int(round(days_l)),
                "CRITICAL" if days_l < _DAYS_TO_ROP_HIGH else
                "WARNING"  if days_l < _DAYS_TO_ROP_WARNING else
                "NOTICE"   if days_l < _DAYS_TO_ROP_NOTICE  else "INFO",
            ))
        else:
            watchlist.append(_w(
                "INVENTORY",
                f"Local inventory is below reorder point — procurement is overdue.",
                f"Current: {local_inv:,.0f} t  |  ROP: {LOCAL_ROP_TONS:,.0f} t",
                0,
                "CRITICAL",
            ))

    if imported_inv > 0:
        days_i = (imported_inv - IMPORTED_ROP_TONS) / DAILY_CONSUMPTION_IMPORTED
        if days_i > 0:
            watchlist.append(_w(
                "INVENTORY",
                f"Imported inventory will reach reorder point in ~{days_i:.0f} days.",
                f"Current: {imported_inv:,.0f} t  |  ROP: {IMPORTED_ROP_TONS:,.0f} t  |  "
                f"Consumption: {DAILY_CONSUMPTION_IMPORTED:.1f} t/day  |  "
                f"Lead time: {IMPORTED_LEAD_TIME_DAYS} days",
                int(round(days_i)),
                "CRITICAL" if days_i < _DAYS_TO_ROP_HIGH else
                "WARNING"  if days_i < _DAYS_TO_ROP_WARNING else
                "NOTICE"   if days_i < _DAYS_TO_ROP_NOTICE  else "INFO",
            ))
        else:
            watchlist.append(_w(
                "INVENTORY",
                "Imported inventory is below reorder point — procurement is overdue.",
                f"Current: {imported_inv:,.0f} t  |  ROP: {IMPORTED_ROP_TONS:,.0f} t  |  "
                f"Import lead time is {IMPORTED_LEAD_TIME_DAYS} days.",
                0,
                "CRITICAL",
            ))

    # ── Mix position ─────────────────────────────────────────────────────────
    if total_inv > 0:
        actual_local_pct = local_inv / total_inv * 100
        dev = actual_local_pct - LOCAL_MIX_TARGET * 100
        if abs(dev) >= _MIX_DEV_NOTICE:
            direction = "above" if dev > 0 else "below"
            watchlist.append(_w(
                "INVENTORY",
                f"Local cotton mix has drifted {abs(dev):.1f} pp {direction} the "
                f"45% target (current: {actual_local_pct:.1f}%).",
                f"Mix target: Local {LOCAL_MIX_TARGET*100:.0f}% / "
                f"Imported {IMPORTED_MIX_TARGET*100:.0f}%.  "
                f"Current: Local {actual_local_pct:.1f}% / "
                f"Imported {100-actual_local_pct:.1f}%.",
                None,
                "WARNING" if abs(dev) >= _MIX_DEV_WARNING else "NOTICE",
            ))

    # ── Price signals ─────────────────────────────────────────────────────────
    for src_key, label in [("local", "Local"), ("imported", "Imported")]:
        sr_dec = scenario_report_dict.get(src_key, {})
        sig    = sr_dec.get("price_signal", PRICE_NEUTRAL)
        dp     = sr_dec.get("price_delta_pct") or 0.0
        curr_p = sr_dec.get("current_price_usd_per_lb")
        fore_p = sr_dec.get("forecast_price_usd_per_lb")
        if sig != PRICE_NEUTRAL and dp:
            direction = "rising" if sig == PRICE_RISING else "falling"
            curr_str  = f"USD {curr_p:.3f}/lb" if curr_p else "—"
            fore_str  = f"USD {fore_p:.3f}/lb" if fore_p else "—"
            watchlist.append(_w(
                "PRICE",
                f"{label} cotton price is {direction} ({dp:+.1f}%).  "
                f"Current: {curr_str}  |  Forecast: {fore_str}.",
                f"Signal: {sig}  |  Delta: {dp:+.1f}%  |  "
                f"Procurement recommendation adjusted accordingly.",
                None,
                "WARNING" if abs(dp) >= _PRICE_DELTA_HIGH_VOLATILITY else "NOTICE",
            ))

    # ── Upcoming order and arrival dates ──────────────────────────────────────
    for src_key, label in [("local", "Local"), ("imported", "Imported")]:
        dec       = exec_report_dict.get(src_key, {})
        action_c  = dec.get("action_code", "HOLD")
        order_dt  = dec.get("order_date")
        arrival   = dec.get("expected_arrival")
        defer_dt  = dec.get("defer_until")
        qty_now   = dec.get("qty_now_tons", 0)

        if action_c in ("BUY_NOW", "BUY_FORWARD", "BUY_SPLIT") and order_dt:
            try:
                od    = date.fromisoformat(order_dt)
                days  = (od - today).days
                dstr  = f"in {days} days ({od})" if days > 0 else f"today ({od})"
                arr_s = f"  Arrival: {arrival}" if arrival else ""
                watchlist.append(_w(
                    "TIMING",
                    f"{label} purchase order due {dstr} — {qty_now:,.0f} t.",
                    f"Action: {dec.get('action','—')}  |  Qty: {qty_now:,.0f} t"
                    f"{arr_s}",
                    max(0, days),
                    dec.get("urgency", "LOW"),
                ))
            except ValueError:
                pass

        if action_c == "DEFER" and defer_dt:
            watchlist.append(_w(
                "TIMING",
                f"{label} procurement deferred — monitor market until {defer_dt}.",
                f"Defer until: {defer_dt}  |  "
                f"Financial benefit: {dec.get('expected_cost_impact',{}).get('narrative','')}",
                None,
                "INFO",
            ))

    # ── Financial opportunities from HIGH/CRITICAL alerts ─────────────────────
    for alert in alerts:
        if alert.alert_id.endswith("_SAV_NOT") or alert.alert_id.endswith("_SAV_INF"):
            watchlist.append(_w(
                "OPPORTUNITY",
                f"Saving opportunity: {alert.title}",
                alert.reason,
                None,
                alert.severity,
            ))
        if alert.alert_id.endswith("_COST_NOT") or alert.alert_id.endswith("_COST_WAR"):
            watchlist.append(_w(
                "RISK",
                f"Cost exposure: {alert.title}",
                alert.reason,
                None,
                alert.severity,
            ))

    # Sort: soonest upcoming events first, then by severity
    def _sort_key(w: WatchlistItem):
        days = w.days_until_event if w.days_until_event is not None else 999
        sev  = _SEV_RANK.get(w.severity, 4)
        return (sev, days)

    watchlist.sort(key=_sort_key)
    return watchlist


