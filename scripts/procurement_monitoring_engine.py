def _build_upcoming_events(exec_report_dict: dict, today: date) -> list:
    events = []
    for src_key, label in [("local", "LOCAL"), ("imported", "IMPORTED")]:
        dec      = exec_report_dict.get(src_key, {})
        action_c = dec.get("action_code", "HOLD")
        order_dt = dec.get("order_date")
        arrival  = dec.get("expected_arrival")
        qty_now  = dec.get("qty_now_tons", 0)
        urgency  = dec.get("urgency", "LOW")

        if action_c in ("BUY_NOW", "BUY_FORWARD", "BUY_SPLIT") and order_dt:
            try:
                od   = date.fromisoformat(order_dt)
                days = (od - today).days
                events.append({
                    "source":      label,
                    "event_type":  "PURCHASE_ORDER",
                    "date":        order_dt,
                    "days_from_now": days,
                    "quantity_tons": qty_now,
                    "urgency":     urgency,
                    "description": (
                        f"Place {label.lower()} cotton purchase order — "
                        f"{qty_now:,.0f} t"
                    ),
                    "arrival_date": arrival,
                })
            except ValueError:
                pass

    events.sort(key=lambda e: e.get("days_from_now", 9999))
    return events


def _build_risk_events(alerts: list, exec_report_dict: dict, today: date) -> list:
    events = []
    for src_key, label in [("local", "LOCAL"), ("imported", "IMPORTED")]:
        inv_dec = exec_report_dict.get(src_key, {})
        risk    = inv_dec.get("risk", {})
        risk_lv = risk.get("level", "LOW")
        if risk_lv in ("CRITICAL", "HIGH"):
            events.append({
                "source":      label,
                "risk_level":  risk_lv,
                "headline":    risk.get("headline", ""),
                "detail":      risk.get("detail", ""),
                "if_ignored":  risk.get("if_ignored", ""),
                "expected_urgency": inv_dec.get("urgency", "LOW"),
            })

    for a in alerts:
        if _SEV_RANK.get(a.severity, 9) <= 1:  # CRITICAL or HIGH
            events.append({
                "source":          a.source,
                "risk_level":      a.severity,
                "headline":        a.title,
                "detail":          a.reason,
                "if_ignored":      a.business_impact,
                "expected_urgency": a.expected_urgency,
            })

    return events


def _build_savings_opps(
    alerts: list,
    exec_report_dict: dict,
    today: date,
) -> list:
    opps = []
    seen = set()

    for a in alerts:
        if "_SAV_" in a.alert_id and a.alert_id not in seen:
            seen.add(a.alert_id)
            opps.append({
                "source":      a.source,
                "type":        "PRICE_DEFERRAL",
                "amount_usd":  a.metric_value,
                "headline":    a.title,
                "detail":      a.reason,
                "action":      a.recommended_action,
                "urgency":     a.expected_urgency,
            })

    for src_key, label in [("local", "LOCAL"), ("imported", "IMPORTED")]:
        dec  = exec_report_dict.get(src_key, {})
        fi   = dec.get("expected_cost_impact", {})
        fi_t = fi.get("impact_type", "NONE")
        av   = fi.get("cost_avoidance_usd") or 0
        if fi_t == "COST_AVOIDANCE" and av >= _COST_INCREASE_NOTICE:
            opps.append({
                "source":      label,
                "type":        "BUY_NOW_AVOIDANCE",
                "amount_usd":  round(av, 0),
                "headline":    f"USD {av:,.0f} cost avoided by buying {label.lower()} now",
                "detail":      fi.get("narrative", ""),
                "action":      f"Follow the {label.lower()} BUY recommendation.",
                "urgency":     "Act before price rises",
            })

    return opps


