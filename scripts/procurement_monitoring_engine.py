def _a(alert_id, title, severity, source, reason, business_impact,
       recommended_action, expected_urgency, metric_value=None,
       threshold_value=None, today=None):
    """Shorthand constructor for MonitoringAlert."""
    return MonitoringAlert(
        alert_id=alert_id,
        title=title,
        severity=severity,
        source=source,
        reason=reason,
        business_impact=business_impact,
        recommended_action=recommended_action,
        expected_urgency=expected_urgency,
        metric_value=metric_value,
        threshold_value=threshold_value,
        triggered_at=(today or date.today()).isoformat(),
    )


def _check_inventory_approaching_rop(
    source: str,
    inventory: float,
    rop: float,
    daily_rate: float,
    today: date,
) -> list:
    alerts = []
    sid = source[:3]  # LOC / IMP
    if inventory <= 0 or daily_rate <= 0:
        return alerts

    if inventory <= rop:
        # Already at or below ROP — status engine handles the action; we flag it here
        days_below = round((rop - inventory) / daily_rate, 1)
        alerts.append(_a(
            f"{sid}_ROP_001",
            f"{source} inventory below reorder point",
            "CRITICAL",
            source,
            f"{source} inventory ({inventory:,.0f} t) is {rop - inventory:,.0f} t below "
            f"the approved reorder point of {rop:,.0f} t.  "
            f"This is equivalent to {days_below:.1f} days of unfilled cover.",
            "Procurement pipeline must be active.  Any further delay risks production shutdown.",
            "Confirm an active purchase order is in progress.  "
            "Escalate immediately if no order has been placed.",
            "Immediate",
            metric_value=inventory,
            threshold_value=rop,
            today=today,
        ))
        return alerts

    days_to_rop = (inventory - rop) / daily_rate
    if days_to_rop < _DAYS_TO_ROP_HIGH:
        sev = "HIGH"
        urgency = "Within 1–2 weeks"
    elif days_to_rop < _DAYS_TO_ROP_WARNING:
        sev = "WARNING"
        urgency = "Within 3 weeks"
    elif days_to_rop < _DAYS_TO_ROP_NOTICE:
        sev = "NOTICE"
        urgency = "Within 6 weeks"
    else:
        return alerts

    alerts.append(_a(
        f"{sid}_ROP_{sev[:3]}",
        f"{source} inventory approaching reorder point",
        sev,
        source,
        f"{source} inventory ({inventory:,.0f} t) will reach the reorder point "
        f"({rop:,.0f} t) in approximately {days_to_rop:.0f} days at current "
        f"consumption of {daily_rate:.1f} t/day.",
        "Procurement window is narrowing.  "
        "Delay beyond this point increases shortage risk and reduces negotiating flexibility.",
        f"Review the active procurement plan.  "
        f"Confirm a purchase order will be placed before the reorder threshold is reached.",
        urgency,
        metric_value=round(days_to_rop, 1),
        threshold_value=float(rop),
        today=today,
    ))
    return alerts


def _check_safety_stock_breach(
    source: str,
    inventory: float,
    safety_stock: float,
    daily_rate: float,
    today: date,
) -> list:
    alerts = []
    sid = source[:3]
    if inventory <= 0:
        return alerts

    if inventory < safety_stock:
        days_below = round((safety_stock - inventory) / daily_rate, 1)
        alerts.append(_a(
            f"{sid}_SS_001",
            f"{source} inventory below safety stock",
            "CRITICAL",
            source,
            f"{source} inventory ({inventory:,.0f} t) has breached the approved safety "
            f"stock floor of {safety_stock:,.0f} t — a shortfall of "
            f"{safety_stock - inventory:,.0f} t ({days_below:.1f} days).",
            "Safety stock exists to absorb demand spikes and supply delays.  "
            "Operating below it exposes the mill to production disruption with no buffer.",
            "Prioritise restocking immediately.  "
            "Notify production planning of reduced buffer.  "
            "Escalate to supply chain director.",
            "Immediate",
            metric_value=inventory,
            threshold_value=safety_stock,
            today=today,
        ))
    elif inventory < safety_stock * (1 + _SS_BREACH_BUFFER_PCT):
        alerts.append(_a(
            f"{sid}_SS_HIGH",
            f"{source} inventory near safety stock floor",
            "HIGH",
            source,
            f"{source} inventory ({inventory:,.0f} t) is within 20% of the safety "
            f"stock floor ({safety_stock:,.0f} t).",
            "Continued drawdown without replenishment will breach the safety buffer.",
            "Accelerate procurement.  Verify there are no supplier delays.",
            "Within 1 week",
            metric_value=inventory,
            threshold_value=safety_stock,
            today=today,
        ))
    return alerts


def _check_lead_time_exposure(
    imported_inv: float,
    imported_days_cover: float,
    today: date,
) -> list:
    """Fire if imported inventory cannot cover the full import lead time plus safety days."""
    alerts = []
    required_cover = IMPORTED_LEAD_TIME_DAYS + MIN_STOCK_DAYS
    days_until_must_order = imported_days_cover - required_cover

    if days_until_must_order <= 0:
        alerts.append(_a(
            "IMP_LT_001",
            "Imported lead-time window breached",
            "CRITICAL",
            "IMPORTED",
            f"Imported inventory cover ({imported_days_cover:.0f} days) is below the "
            f"required cover of {required_cover} days (lead time {IMPORTED_LEAD_TIME_DAYS}d "
            f"+ safety {MIN_STOCK_DAYS}d).  An order placed today will arrive after "
            f"stock is exhausted.",
            "Production may face an imported cotton shortage before the next order arrives.",
            "Issue an emergency purchase order immediately.  "
            "Evaluate expedited shipping options.",
            "Immediate",
            metric_value=imported_days_cover,
            threshold_value=float(required_cover),
            today=today,
        ))
    elif days_until_must_order < 14:
        alerts.append(_a(
            "IMP_LT_HIGH",
            "Imported order must be placed within 14 days",
            "HIGH",
            "IMPORTED",
            f"Imported inventory ({imported_inv:,.0f} t, {imported_days_cover:.0f} days) "
            f"requires a purchase order within {days_until_must_order:.0f} days to avoid "
            f"a gap on arrival.",
            "Missing this window will create an imported cotton gap on the production floor.",
            "Confirm the imported purchase order date.  "
            "Do not defer beyond this threshold.",
            "Within 2 weeks",
            metric_value=round(days_until_must_order, 1),
            threshold_value=14.0,
            today=today,
        ))
    elif days_until_must_order < 30:
        alerts.append(_a(
            "IMP_LT_WARN",
            "Imported procurement window is closing",
            "WARNING",
            "IMPORTED",
            f"Imported order must be placed within approximately {days_until_must_order:.0f} "
            f"days to maintain cover through the {IMPORTED_LEAD_TIME_DAYS}-day import lead time.",
            "Delayed ordering will compress the safety buffer on the next import cycle.",
            "Review the imported procurement schedule.  "
            "Ensure supplier capacity and shipping is confirmed.",
            "Within 4 weeks",
            metric_value=round(days_until_must_order, 1),
            threshold_value=30.0,
            today=today,
        ))
    return alerts


def _check_mix_deviation(
    local_inv: float,
    total_inv: float,
    today: date,
) -> list:
    alerts = []
    if total_inv <= 0:
        return alerts

    actual_local_pct  = local_inv / total_inv * 100.0
    actual_imp_pct    = 100.0 - actual_local_pct
    target_local_pct  = LOCAL_MIX_TARGET * 100.0   # 45.0
    target_imp_pct    = IMPORTED_MIX_TARGET * 100.0  # 55.0

    local_dev = actual_local_pct - target_local_pct
    imp_dev   = actual_imp_pct   - target_imp_pct

    for dev, dev_label, target, source_key, sid in [
        (local_dev,  "local",    target_local_pct, "LOCAL",    "LOC"),
        (imp_dev,    "imported", target_imp_pct,   "IMPORTED", "IMP"),
    ]:
        abs_dev = abs(dev)
        if abs_dev < _MIX_DEV_NOTICE:
            continue
        if abs_dev >= _MIX_DEV_HIGH:
            sev, urgency = "HIGH", "This week"
        elif abs_dev >= _MIX_DEV_WARNING:
            sev, urgency = "WARNING", "Within 2 weeks"
        else:
            sev, urgency = "NOTICE", "Within 4 weeks"

        direction = "above" if dev > 0 else "below"
        alerts.append(_a(
            f"{sid}_MIX_{sev[:3]}",
            f"{dev_label.title()} cotton mix drifting {direction} target",
            sev,
            source_key,
            f"Current {dev_label} cotton share is {actual_local_pct if dev_label=='local' else actual_imp_pct:.1f}% "
            f"vs the approved target of {target:.0f}%.  "
            f"Deviation: {abs_dev:.1f} percentage points.",
            f"Mix drift increases dependency on a single source, raising supply concentration risk.  "
            f"It may also violate blending commitments or quality contracts.",
            f"{'Increase' if dev < 0 else 'Reduce'} {dev_label} cotton procurement to rebalance "
            f"toward the {target:.0f}% target over the next procurement cycle.",
            urgency,
            metric_value=round(actual_local_pct if dev_label == "local" else actual_imp_pct, 1),
            threshold_value=target,
            today=today,
        ))
    return alerts


def _check_storage_capacity(
    total_inv: float,
    max_storage: float,
    today: date,
) -> list:
    alerts = []
    if max_storage is None or max_storage <= 0:
        return alerts

    utilisation = total_inv / max_storage
    if utilisation >= _STORAGE_PCT_WARNING:
        sev, urgency = "WARNING", "Immediately"
    elif utilisation >= _STORAGE_PCT_NOTICE:
        sev, urgency = "NOTICE", "This week"
    elif utilisation >= _STORAGE_PCT_INFO:
        sev, urgency = "INFO", "This month"
    else:
        return alerts

    alerts.append(_a(
        f"PORT_STOR_{sev[:3]}",
        f"Storage utilisation at {utilisation*100:.0f}%",
        sev,
        "PORTFOLIO",
        f"Total inventory ({total_inv:,.0f} t) represents {utilisation*100:.1f}% of "
        f"maximum declared storage capacity ({max_storage:,.0f} t).",
        "Storage nearing capacity constrains opportunistic forward buying and "
        "may create logistical bottlenecks during peak production periods.",
        "Review storage utilisation with warehouse team.  "
        "Defer any non-mandatory forward purchases until capacity is cleared.",
        urgency,
        metric_value=round(utilisation * 100, 1),
        threshold_value=_STORAGE_PCT_NOTICE * 100,
        today=today,
    ))
    return alerts


def _check_forecast_confidence(
    exec_report_dict: dict,
    today: date,
) -> list:
    alerts = []
    for source_key in ("local", "imported"):
        dec  = exec_report_dict.get(source_key, {})
        conf = dec.get("confidence", {})
        score = conf.get("score", 100)
        label = conf.get("label", "HIGH")
        sid   = source_key[:3].upper()

        if score < _CONF_SCORE_WARNING:
            sev, urgency = "WARNING", "Review before acting"
        elif score < _CONF_SCORE_NOTICE:
            sev, urgency = "NOTICE", "Monitor closely"
        elif score < _CONF_SCORE_INFO:
            sev, urgency = "INFO", "Watch trend"
        else:
            continue

        alerts.append(_a(
            f"{sid}_CONF_{sev[:3]}",
            f"{source_key.title()} procurement confidence low ({score}/100)",
            sev,
            source_key.upper(),
            f"The decision engine's confidence in the {source_key} recommendation is "
            f"{score}/100 ({label}).  "
            f"Low confidence typically reflects uncertain price signals, "
            f"inventory at a borderline status, or limited forward visibility.",
            "Lower confidence means the recommendation has a higher probability of "
            "being revised if market conditions shift.  "
            "Management should treat the action with more caution.",
            "Request an updated price forecast.  "
            "Consider a smaller initial order with a planned follow-on review "
            "in 7–10 days.",
            urgency,
            metric_value=float(score),
            threshold_value=float(_CONF_SCORE_NOTICE),
            today=today,
        ))
    return alerts


def _check_forecast_direction(
    scenario_report_dict: dict,
    today: date,
) -> list:
    """Alert on conflicting signals between sources or high price volatility."""
    alerts = []
    local_sig  = scenario_report_dict.get("local",    {}).get("price_signal", PRICE_NEUTRAL)
    imp_sig    = scenario_report_dict.get("imported", {}).get("price_signal", PRICE_NEUTRAL)
    local_dp   = scenario_report_dict.get("local",    {}).get("price_delta_pct") or 0.0
    imp_dp     = scenario_report_dict.get("imported", {}).get("price_delta_pct") or 0.0
    local_conf = scenario_report_dict.get("local",    {}).get("price_confidence", "HIGH")
    imp_conf   = scenario_report_dict.get("imported", {}).get("price_confidence", "HIGH")

    # Conflicting directional signals between local and imported
    if local_sig != imp_sig and PRICE_NEUTRAL not in (local_sig, imp_sig):
        alerts.append(_a(
            "PORT_SIG_CONF",
            "Conflicting price signals: local vs imported",
            "NOTICE",
            "PORTFOLIO",
            f"Local cotton price signal is {local_sig} ({local_dp:+.1f}%) while "
            f"imported cotton signal is {imp_sig} ({imp_dp:+.1f}%).  "
            f"The two sources are pointing in opposite directions.",
            "Conflicting signals complicate the optimal buy/defer decision across "
            "the blended portfolio.  One position may partially offset the other.",
            "Seek updated market intelligence for both sources.  "
            "Consider a split-purchase strategy to hedge the directional uncertainty.",
            "Before next order",
            metric_value=abs(local_dp - imp_dp),
            threshold_value=None,
            today=today,
        ))

    # High-magnitude price movement (high volatility)
    for dp, src, conf in [(local_dp, "LOCAL", local_conf), (imp_dp, "IMPORTED", imp_conf)]:
        if abs(dp) >= _PRICE_DELTA_HIGH_VOLATILITY:
            alerts.append(_a(
                f"{src[:3]}_VOL_WARN",
                f"High {src.lower()} price volatility detected ({dp:+.1f}%)",
                "WARNING",
                src,
                f"The {src.lower()} cotton price forecast implies a {dp:+.1f}% "
                f"move from current price.  Forecast confidence: {conf}.",
                "High volatility increases the financial risk of committing to a "
                "large purchase quantity in either direction.  "
                "Actual costs may deviate significantly from model estimates.",
                "Validate the forecast with a second independent source.  "
                "Consider reducing order size and splitting across two procurement dates.",
                "Before committing orders",
                metric_value=round(abs(dp), 1),
                threshold_value=_PRICE_DELTA_HIGH_VOLATILITY,
                today=today,
            ))

    # Low-confidence price signal
    for src, conf, dp in [("LOCAL", local_conf, local_dp), ("IMPORTED", imp_conf, imp_dp)]:
        if conf == "LOW":
            alerts.append(_a(
                f"{src[:3]}_PCONF_INFO",
                f"{src.lower().title()} price confidence is LOW",
                "INFO",
                src,
                f"Price confidence for {src.lower()} cotton is LOW "
                f"(forecast delta {dp:+.1f}%).  "
                "This may reflect insufficient historical data or a very recent market event.",
                "The price-signal component of the recommendation carries higher uncertainty.",
                "Treat price-driven procurement adjustments (forward buy, deferral) with caution.",
                "Next price update",
                metric_value=None,
                threshold_value=None,
                today=today,
            ))
    return alerts


def _check_cost_increase(
    exec_report_dict: dict,
    today: date,
) -> list:
    """Alert when a large cost avoidance opportunity exists (act now to avoid future cost)."""
    alerts = []
    for source_key in ("local", "imported"):
        dec   = exec_report_dict.get(source_key, {})
        fi    = dec.get("expected_cost_impact", {})
        fi_type   = fi.get("impact_type", "NONE")
        avoidance = fi.get("cost_avoidance_usd") or 0.0
        sid   = source_key[:3].upper()

        if fi_type != "COST_AVOIDANCE" or avoidance <= 0:
            continue

        if avoidance >= _COST_INCREASE_HIGH:
            sev, urgency = "HIGH", "Act this week"
        elif avoidance >= _COST_INCREASE_WARNING:
            sev, urgency = "WARNING", "Act within 2 weeks"
        elif avoidance >= _COST_INCREASE_NOTICE:
            sev, urgency = "NOTICE", "Act within 1 month"
        else:
            continue

        alerts.append(_a(
            f"{sid}_COST_{sev[:3]}",
            f"USD {avoidance:,.0f} procurement cost exposure on {source_key} cotton",
            sev,
            source_key.upper(),
            f"The price model projects a cost increase of USD {avoidance:,.0f} on "
            f"{source_key} cotton if procurement is deferred beyond the current recommended "
            f"order date.",
            f"Inaction will result in paying an estimated USD {avoidance:,.0f} more "
            f"to procure the same quantity at the projected higher price.",
            "Follow the active procurement recommendation.  "
            "Confirm order authorisation and issue the purchase order "
            "within the recommended window.",
            urgency,
            metric_value=round(avoidance, 0),
            threshold_value=_COST_INCREASE_NOTICE,
            today=today,
        ))
    return alerts


def _check_saving_opportunity(
    exec_report_dict: dict,
    today: date,
) -> list:
    """Alert when a significant saving opportunity exists from deferral."""
    alerts = []
    for source_key in ("local", "imported"):
        dec   = exec_report_dict.get(source_key, {})
        fi    = dec.get("expected_cost_impact", {})
        fi_type = fi.get("impact_type", "NONE")
        saving  = fi.get("expected_savings_usd") or 0.0
        sid     = source_key[:3].upper()

        if fi_type != "SAVING" or saving <= 0:
            continue

        if saving >= _SAVING_OPP_WARNING:
            sev, urgency = "NOTICE", "Capture before market moves"
        elif saving >= _SAVING_OPP_NOTICE:
            sev, urgency = "NOTICE", "Monitor and confirm"
        elif saving >= _SAVING_OPP_INFO:
            sev, urgency = "INFO", "Track market"
        else:
            continue

        alerts.append(_a(
            f"{sid}_SAV_{sev[:3]}",
            f"USD {saving:,.0f} saving opportunity on {source_key} cotton",
            sev,
            source_key.upper(),
            f"The price model projects that deferring the {source_key} order "
            f"will save approximately USD {saving:,.0f} as prices are forecast to fall.",
            f"Buying now would cost an estimated USD {saving:,.0f} more than "
            f"waiting for the projected price reduction.",
            "Monitor prices closely.  "
            "Defer procurement if inventory position allows and risk is manageable.  "
            "Review the defer-until date in the procurement recommendation.",
            urgency,
            metric_value=round(saving, 0),
            threshold_value=_SAVING_OPP_NOTICE,
            today=today,
        ))
    return alerts


def _check_simulation_outperformance(
    best_sim_score: Optional[int],
    prod_score: Optional[int],
    today: date,
) -> list:
    """Alert if a saved what-if simulation substantially outperforms production."""
    alerts = []
    if best_sim_score is None or prod_score is None:
        return alerts

    delta = best_sim_score - prod_score
    if delta < _SIM_OUTPERFORM_NOTICE:
        return alerts

    sev     = "HIGH" if delta >= _SIM_OUTPERFORM_HIGH else "NOTICE"
    urgency = "Review before next procurement cycle" if sev == "HIGH" else "At next planning session"
    alerts.append(_a(
        "PORT_SIM_OPP",
        f"What-If simulation outperforms production by {delta} pts",
        sev,
        "PORTFOLIO",
        f"A saved What-If simulation achieved an overall strategy score of "
        f"{best_sim_score}/100 vs the current production recommendation of "
        f"{prod_score}/100 — a delta of {delta} points.",
        "There may be an alternative parameter configuration that produces a "
        "materially better procurement outcome under current market conditions.",
        "Open the What-If Lab tab and review the saved simulation.  "
        "If the parameters are realistic, consider updating the procurement plan.",
        urgency,
        metric_value=float(delta),
        threshold_value=float(_SIM_OUTPERFORM_NOTICE),
        today=today,
    ))
    return alerts


def _check_stale_data(
    local_inv: float,
    imported_inv: float,
    total_inv: float,
    today: date,
) -> list:
    """Alert on data quality issues that would corrupt the monitoring output."""
    alerts = []

    # Total inventory is zero
    if total_inv <= 0:
        alerts.append(_a(
            "SYS_DATA_001",
            "Total inventory reads as zero — data may be missing",
            "CRITICAL",
            "SYSTEM",
            "Total inventory is 0 tons.  This is not a valid operational reading.",
            "All procurement recommendations derived from zero inventory will be incorrect.",
            "Refresh the Oracle inventory data feed.  "
            "Do not act on procurement recommendations until the data is validated.",
            "Immediate",
            metric_value=0.0,
            threshold_value=None,
            today=today,
        ))
        return alerts

    # Consistency check: local + imported should sum to total (within 1%)
    computed_total = local_inv + imported_inv
    if abs(computed_total - total_inv) / max(total_inv, 1) > 0.01:
        alerts.append(_a(
            "SYS_DATA_002",
            "Inventory totals are inconsistent",
            "WARNING",
            "SYSTEM",
            f"Local ({local_inv:,.0f} t) + Imported ({imported_inv:,.0f} t) = "
            f"{computed_total:,.0f} t, but total inventory is reported as "
            f"{total_inv:,.0f} t.  Discrepancy: {abs(computed_total - total_inv):,.0f} t.",
            "Inconsistent inventory data may cause incorrect status classification "
            "and flawed procurement recommendations.",
            "Investigate the Oracle data pipeline for a source reconciliation error.  "
            "Do not place orders until the data is validated.",
            "Before acting on recommendations",
            metric_value=round(abs(computed_total - total_inv), 0),
            threshold_value=0.0,
            today=today,
        ))

    # Implausibly large inventory
    if total_inv > 100_000:
        alerts.append(_a(
            "SYS_DATA_003",
            f"Total inventory unusually high ({total_inv:,.0f} t)",
            "WARNING",
            "SYSTEM",
            f"Total inventory of {total_inv:,.0f} t exceeds 100,000 t, which is "
            f"implausibly large for this facility.  This may indicate a data error.",
            "Inflated inventory would suppress procurement urgency and delay necessary orders.",
            "Validate the Oracle data.  "
            "Check for unit conversion errors (e.g., kg reported as t).",
            "Before acting on recommendations",
            metric_value=round(total_inv, 0),
            threshold_value=100_000.0,
            today=today,
        ))

    return alerts


