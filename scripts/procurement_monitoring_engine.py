# ---------------------------------------------------------------------------
# Severity ordering (lower rank = more severe)
# ---------------------------------------------------------------------------
_SEV_RANK = {
    "CRITICAL": 0,
    "HIGH":     1,
    "WARNING":  2,
    "NOTICE":   3,
    "INFO":     4,
}

# ---------------------------------------------------------------------------
# Alert thresholds — tunable without changing any procurement calculations
# ---------------------------------------------------------------------------
_DAYS_TO_ROP_NOTICE   = 45   # days before reaching ROP → NOTICE
_DAYS_TO_ROP_WARNING  = 21   # days before reaching ROP → WARNING
_DAYS_TO_ROP_HIGH     = 10   # days before reaching ROP → HIGH

_SS_BREACH_BUFFER_PCT = 0.20  # fire HIGH when within 20% of safety stock

_MIX_DEV_NOTICE  =  5.0   # percentage-point deviation from mix target
_MIX_DEV_WARNING = 10.0
_MIX_DEV_HIGH    = 15.0

_STORAGE_PCT_INFO    = 0.70
_STORAGE_PCT_NOTICE  = 0.80
_STORAGE_PCT_WARNING = 0.90

_CONF_SCORE_INFO    = 75   # confidence below 75 → INFO
_CONF_SCORE_NOTICE  = 65   # confidence below 65 → NOTICE
_CONF_SCORE_WARNING = 50   # confidence below 50 → WARNING

_PRICE_DELTA_HIGH_VOLATILITY = 20.0   # |delta %| above this → WARNING (volatility)

_COST_INCREASE_NOTICE  =  200_000   # USD cost avoidance threshold → NOTICE
_COST_INCREASE_WARNING =  500_000
_COST_INCREASE_HIGH    = 1_000_000

_SAVING_OPP_INFO    =   50_000   # USD saving opportunity threshold → INFO
_SAVING_OPP_NOTICE  =  100_000
_SAVING_OPP_WARNING =  500_000

_SIM_OUTPERFORM_NOTICE = 8    # score delta points before firing NOTICE
_SIM_OUTPERFORM_HIGH   = 20   # score delta points before firing HIGH


