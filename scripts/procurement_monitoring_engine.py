def _build_summary(
    alerts: list,
    exec_report_dict: dict,
    highest_severity: str,
    today: date,
) -> str:
    n_total    = len(alerts)
    n_critical = sum(1 for a in alerts if a.severity == "CRITICAL")
    n_high     = sum(1 for a in alerts if a.severity == "HIGH")
    port_urg   = exec_report_dict.get("portfolio_urgency", "LOW")

    if n_critical:
        opening = (
            f"CRITICAL monitoring status.  {n_critical} critical alert(s) require "
            f"immediate executive attention."
        )
    elif n_high:
        opening = (
            f"Elevated monitoring status — {n_high} HIGH-severity alert(s) detected."
        )
    elif highest_severity in ("WARNING", "NOTICE"):
        opening = (
            f"Monitoring status: {highest_severity}.  "
            f"{n_total} alert(s) active across the procurement portfolio."
        )
    else:
        opening = "No critical or high-severity alerts.  Portfolio monitoring is in a normal state."

    port_str = (
        f"Portfolio procurement urgency is {port_urg}."
        if port_urg else ""
    )
    guidance = (
        "Review the alert list and watchlist below before authorising any procurement actions."
        if n_total > 0 else
        "Continue to monitor price signals and inventory levels on the standard cycle."
    )
    return f"{opening}  {port_str}  {guidance}".strip()


