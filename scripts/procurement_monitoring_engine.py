"""
procurement_monitoring_engine.py
---------------------------------
PSE-7A — Executive Monitoring & Early Warning System.

Consumes outputs from PSE-5A (ScenarioReport) and PSE-5B (ExecutiveReport)
to generate proactive alerts and an executive watchlist.

Purpose: not to make procurement decisions, but to answer
    "What should management pay attention to next?"

Does NOT recalculate procurement quantities.
Does NOT modify scenario logic or forecasting logic.
Reads production constants (ROP, safety stock, mix targets) as read-only
thresholds — no formulas are duplicated or overridden.

Public API
----------
    generate_monitoring_report(exec_report_dict, scenario_report_dict,
                               local_inventory_tons, imported_inventory_tons,
                               total_inventory_tons, today, ...)
        → ExecutiveMonitoringReport

    run_pse7a(workbook_path, ...)
        → ExecutiveMonitoringReport
"""
