def generate_monitoring_report(
    exec_report_dict: dict,
    scenario_report_dict: dict,
    local_inventory_tons: float,
    imported_inventory_tons: float,
    total_inventory_tons: float,
    today: Optional[date] = None,
    max_storage_tons: Optional[float] = None,
    best_sim_score: Optional[int] = None,
    prod_score: Optional[int] = None,
) -> ExecutiveMonitoringReport:
    """
    Generate an executive monitoring report from existing engine outputs.

    Parameters
    ----------
    exec_report_dict     : ExecutiveReport.to_dict() from PSE-5B
    scenario_report_dict : ScenarioReport.to_dict() from PSE-5A
    local_inventory_tons : current local cotton inventory (tons)
    imported_inventory_tons : current imported cotton inventory (tons)
    total_inventory_tons : total inventory (tons)
    today                : evaluation date (defaults to date.today())
    max_storage_tons     : declared warehouse capacity (optional)
    best_sim_score       : highest scenario score from What-If lab (optional)
    prod_score           : current production scenario score (optional)
    """
    today = today or date.today()

    # ── Run all checkers ─────────────────────────────────────────────────────
    alerts: list[MonitoringAlert] = []

    alerts += _check_stale_data(
        local_inventory_tons, imported_inventory_tons, total_inventory_tons, today)

    # Only continue if data is sane
    if total_inventory_tons > 0:
        alerts += _check_safety_stock_breach(
            "LOCAL", local_inventory_tons, SAFETY_STOCK_LOCAL_TONS,
            DAILY_CONSUMPTION_LOCAL, today)
        alerts += _check_safety_stock_breach(
            "IMPORTED", imported_inventory_tons, SAFETY_STOCK_IMPORTED_TONS,
            DAILY_CONSUMPTION_IMPORTED, today)

        alerts += _check_inventory_approaching_rop(
            "LOCAL", local_inventory_tons, float(LOCAL_ROP_TONS),
            DAILY_CONSUMPTION_LOCAL, today)
        alerts += _check_inventory_approaching_rop(
            "IMPORTED", imported_inventory_tons, float(IMPORTED_ROP_TONS),
            DAILY_CONSUMPTION_IMPORTED, today)

        local_dc   = local_inventory_tons    / DAILY_CONSUMPTION_LOCAL
        imported_dc = imported_inventory_tons / DAILY_CONSUMPTION_IMPORTED
        alerts += _check_lead_time_exposure(
            imported_inventory_tons, imported_dc, today)

        alerts += _check_mix_deviation(
            local_inventory_tons, total_inventory_tons, today)

        if max_storage_tons:
            alerts += _check_storage_capacity(
                total_inventory_tons, max_storage_tons, today)

    alerts += _check_forecast_confidence(exec_report_dict, today)
    alerts += _check_forecast_direction(scenario_report_dict, today)
    alerts += _check_cost_increase(exec_report_dict, today)
    alerts += _check_saving_opportunity(exec_report_dict, today)
    alerts += _check_simulation_outperformance(best_sim_score, prod_score, today)

    # De-duplicate: keep most severe alert per base ID prefix
    seen_prefix: dict[str, int] = {}
    deduped: list[MonitoringAlert] = []
    for a in alerts:
        prefix = a.alert_id.rsplit("_", 1)[0]
        rank   = _SEV_RANK.get(a.severity, 99)
        if prefix not in seen_prefix or rank < seen_prefix[prefix]:
            seen_prefix[prefix] = rank
            deduped = [x for x in deduped if x.alert_id.rsplit("_", 1)[0] != prefix]
            deduped.append(a)

    deduped.sort(key=lambda a: (_SEV_RANK.get(a.severity, 99), a.source, a.alert_id))

    # ── Summary metrics ───────────────────────────────────────────────────────
    count_by_sev: dict[str, int] = {s: 0 for s in _SEV_RANK}
    for a in deduped:
        count_by_sev[a.severity] = count_by_sev.get(a.severity, 0) + 1

    highest_sev = "INFO"
    for sev in ("CRITICAL", "HIGH", "WARNING", "NOTICE", "INFO"):
        if count_by_sev.get(sev, 0) > 0:
            highest_sev = sev
            break

    watchlist = _build_watchlist(
        deduped, exec_report_dict, scenario_report_dict,
        local_inventory_tons, imported_inventory_tons, total_inventory_tons, today,
    )

    return ExecutiveMonitoringReport(
        run_date=today.isoformat(),
        alerts=deduped,
        watchlist=watchlist,
        upcoming_procurement_events=_build_upcoming_events(exec_report_dict, today),
        upcoming_risk_events=_build_risk_events(deduped, exec_report_dict, today),
        upcoming_savings_opportunities=_build_savings_opps(deduped, exec_report_dict, today),
        summary=_build_summary(deduped, exec_report_dict, highest_sev, today),
        highest_severity=highest_sev,
        alert_count_by_severity=count_by_sev,
    )


def run_pse7a(
    workbook_path: Optional[str] = None,
    input_path: Optional[str] = None,
    today: Optional[date] = None,
    max_storage_tons: Optional[float] = None,
    best_sim_score: Optional[int] = None,
    prod_score: Optional[int] = None,
) -> ExecutiveMonitoringReport:
    """
    Convenience runner: runs PSE-5B pipeline then generates the monitoring report.

    Returns ExecutiveMonitoringReport.
    """
    from procurement_orchestrator import run_orchestration
    from procurement_scenario_engine import run_pse5a
    from procurement_decision_engine import generate_executive_report

    today = today or date.today()

    # Single workbook read — passed to run_pse5a via _orch to avoid redundant reads
    orch = run_orchestration(input_path=input_path, workbook_path=workbook_path)
    so   = orch["strategy_output"]

    scenario_report = run_pse5a(
        workbook_path=workbook_path,
        input_path=input_path,
        live_prices=False,
        today=today,
        _orch=orch,
    )

    exec_report = generate_executive_report(
        scenario_report=scenario_report,
        local_status=so.local_status,
        imported_status=so.imported_status,
    )

    return generate_monitoring_report(
        exec_report_dict=exec_report.to_dict(),
        scenario_report_dict=scenario_report.to_dict(),
        local_inventory_tons=float(so.local_inventory_tons),
        imported_inventory_tons=float(so.imported_inventory_tons),
        total_inventory_tons=float(so.total_inventory_tons),
        today=today,
        max_storage_tons=max_storage_tons,
        best_sim_score=best_sim_score,
        prod_score=prod_score,
    )
